package servlets;

public class Carro {
	
	public String modelo;
	public String marca; 
	public Integer anoFabrica;
	
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public Integer getAnoFabrica() {
		return anoFabrica;
	}
	public void setAnoFabricadao(Integer anoFabrica) {
		this.anoFabric = anoFabrica;
	}
	
	
	

}
